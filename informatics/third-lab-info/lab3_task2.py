import re


def count_syllables(line):
    # Используем регулярное выражение для поиска гласных в строке
    return len(re.findall(r'[аеёиоуыэюяАЕЁИОУЫЭЮЯ]', line))


def check_haiku(text):
    # Разделяем текст по символу '/' с помощью регулярного выражения
    lines = re.split(r'\s*/\s*', text.strip())

    # Если строк не 3, выводим ошибку
    if len(lines) != 3:
        print("Не хайку. Должно быть 3 строки.")
        return

    # Используем регулярные выражения для подсчёта слогов в каждой строке
    syllables = [count_syllables(line) for line in lines]

    # Проверяем структуру хайку (5/7/5)
    if syllables == [5, 7, 5]:
        print("Хайку!")
    else:
        print("Не хайку.")

check_haiku("Вечер за окном. / Еще один день прожит. / Жизнь скоротечна...") # Ожидаем Хайку!

check_haiku("Просто текст") # Ожидаем Не хайку. Должно быть 3 строки.

check_haiku("Как вишня расцвела! / Она с коня согнала / И князя-гордеца.") # Ожидаем Не хайку.
